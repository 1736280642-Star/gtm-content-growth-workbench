export function recommendWechatImageStyles(input = {}) {
  const platformContentType = cleanText(input.platformContentType, "", 80);
  const text = [input.title, input.contentType, input.sourceProblem, input.coreJudgment, input.keywords]
    .flat()
    .filter(Boolean)
    .join(" ");
  const scores = new Map(STYLE_IDS.map((styleId) => [styleId, STYLE_REGISTRY[styleId].priorityWeight]));

  const ordered = CONTENT_TYPE_STYLE_ORDER[platformContentType] || [];
  ordered.forEach((styleId, index) => {
    scores.set(styleId, (scores.get(styleId) || 0) + Math.max(1, 5 - index));
  });

  for (const rule of KEYWORD_STYLE_RULES) {
    if (rule.pattern.test(text)) {
      scores.set(rule.styleId, (scores.get(rule.styleId) || 0) + rule.score);
    }
  }

  return [...scores.entries()]
    .sort((left, right) => right[1] - left[1] || STYLE_IDS.indexOf(left[0]) - STYLE_IDS.indexOf(right[0]))
    .slice(0, Math.min(Number(input.limit) || 4, 6))
    .map(([styleId, score]) => ({
      styleId,
      styleName: STYLE_REGISTRY[styleId].name,
      score,
      reason: STYLE_REGISTRY[styleId].bestFor.join("、")
    }));
}

export function validateWechatImageSpec(raw = {}, article = {}) {
  const spec = normalizeSpec(raw, article);
  const errors = [];
  const warnings = [];
  const numericFields = [spec.coreIdea, spec.shortLabels, spec.bottomSentence, spec.displayTitle];

  if (!STYLE_REGISTRY[raw.styleId]) {
    warnings.push(`未知 styleId，已回退为 ${DEFAULT_STYLE_ID}`);
  }
  if (!raw.coreIdea) errors.push("coreIdea 不能为空");
  if (!raw.visualAnchor) errors.push("visualAnchor 不能为空");
  if (!raw.structure) errors.push("structure 不能为空");
  if (!raw.mainAction) errors.push("mainAction 不能为空");
  if (!spec.sceneElements.length) warnings.push("sceneElements 为空，画面稳定性可能下降");
  if (spec.textPolicy === "model_text") warnings.push("model_text 必须人工逐字检查中文，生产默认建议 post_layout");
  if (spec.styleId === "crowd_typography_scene" && !spec.evidenceVerified) {
    warnings.push("人群造字风容易暗示规模或统计结论，需确认画面没有制造未经证据支持的数量感");
  }
  if (numericFields.some(containsNumericClaim) && !spec.evidenceVerified && spec.evidenceClaimIds.length === 0) {
    errors.push("图片方案包含数字或量化主张，但没有 evidenceVerified 或 evidenceClaimIds");
  }

  return { spec, errors, warnings, valid: errors.length === 0 };
}

export function buildWechatImagePrompt(raw = {}, article = {}) {
  const validation = validateWechatImageSpec(raw, article);
  if (!validation.valid) {
    const error = new Error(`图片 Prompt 输入不合法：${validation.errors.join("；")}`);
    error.validation = validation;
    throw error;
  }

  const style = STYLE_REGISTRY[validation.spec.styleId];
  const textInstruction = buildTextInstruction(validation.spec);
  const prompt = style.promptTemplate({ spec: validation.spec, textInstruction });

  return {
    ...validation.spec,
    styleName: style.name,
    promptLibraryVersion: PROMPT_LIBRARY_VERSION,
    productionStatus: "prompt_ready",
    prompt,
    warnings: validation.warnings,
    requiresHumanPlanConfirmation: true,
    requiresHumanPreview: true,
    imageProviderStatus: "pending_config",
    copyAllowed: false,
    distributionAllowed: false
  };
}

